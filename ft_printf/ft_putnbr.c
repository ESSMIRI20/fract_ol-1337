#include <unistd.h>

int	ft_putnbr(int n)
{
	int	r;

	r = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		r += write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		r += ft_putnbr(n / 10);
		r += ft_putnbr(n % 10);
	}
	if (n <= 9)
	{
		n += 48;
		r += write(1, &n, 1);
	}
	return (r);
}
